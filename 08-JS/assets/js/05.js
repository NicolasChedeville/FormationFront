/************************************************
 *       Incrémentation et Décrémentation
 ************************************************/

// -- Incrémentation --
var nb1 = 1;
nb1 = nb1 + 1;
//Simplifié
nb1++;

// -- Décrémentation -- 
var nb1 = 1;
nb1 = nb1 - 1;
//Simplifié
nb1--;

// -- Subtilite --
nb1 = 0;
console.log(nb1++);
nb1 = 0;
console.log(++nb1);